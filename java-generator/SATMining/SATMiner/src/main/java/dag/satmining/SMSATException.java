package dag.satmining;

public class SMSATException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public SMSATException() {
	}

	public SMSATException(String arg0) {
		super(arg0);
	}

	public SMSATException(Throwable arg0) {
		super(arg0);
	}

	public SMSATException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
